#include "libft.h"

#include <stddef.h> // NULL size_t

// Returns the last occurence of "c" in "str"
char	*ft_strrchr(const char *s, int c)
{
	const char	*last;

	last = NULL;
	while (*s)
	{
		if (*s == (char)c)
			last = s;
		s++;
	}
	if ((char)c == '\0')
		last = s;
	return ((char *)last);
}
